    void init () {
        setLocationRelativeTo(this);
        tabs.setSelectedIndex(1);
        setIconImage(XImage.getAppIcon());
        setResizable(false);
        fillTable();
        clearForm();
    }

    private void fillTable () {
        DefaultTableModel model = (DefaultTableModel) tblLesson.getModel();
        model.setRowCount(0);
        try {
            List<Lesson> list = (List<Lesson>) dao.selectAll();
            if (list != null) {
                for (int i = 0; i < list.size(); i++) {
                    Lesson ls = list.get(i);
                    model.addRow(new Object[]{i + 1, ls.getLessonID(), ls.getLessonName(),
                        ls.getDateCreated(), ls.getTeacherID()});
                }
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void fillToForm () {
        int index = (int) tblLesson.getValueAt(this.row, 1);
        Lesson ls = dao.selectById(index);
        this.setForm(ls);
        tabs.setSelectedIndex(0);
    }

    private void setForm (Lesson ls) {
        if (ls.getLessonID() == 0) {
            txtLessonID.setText("");
        }
        else {
            txtLessonID.setText(String.valueOf(ls.getLessonID()));
        }
        cboSubject.setSelectedItem(ls.getSubject());
        txtLessonTitle.setText(ls.getLessonName());
        cboGrade.setSelectedItem(ls.getGrade());
    }

    private Lesson getForm () {
        Lesson ls = new Lesson();
        ls.setSubject(cboSubject.getSelectedItem().toString());
        ls.setLessonName(txtLessonTitle.getText());
        ls.setGrade(Integer.parseInt(cboGrade.getSelectedItem().toString()));
        ls.setTeacherID(Auth.user.getUserID());
        return ls;
    }

    private void insert () {
        Lesson ls = getForm();
        try {
            dao.insert(ls);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    void clearForm () {
        Lesson ls = new Lesson();
        this.setForm(ls);
        updateStatus(true);
        btnEdit.setEnabled(false);
    }

    private void update () {
        Lesson ls = getForm();
        try {
            dao.update(ls);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Sửa thành công");
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Sửa thất bại");
        }
    }

    private void delete () {
        if (MsgBox.confirm(this, "Bạn thực sự muốn xóa bài học này?")) {
            Integer mabh = Integer.parseInt(txtLessonID.getText());
            try {
                dao.delete(mabh);
                this.fillTable();
                this.clearForm();

                MsgBox.alert(this, "Xóa thành công!");
            } catch (HeadlessException e) {
                MsgBox.alert(this, "Xóa thất bại!");
            }
        }
    }

    private void edit () {
        if (Auth.user.getUserID().equals(tblLesson.getValueAt(this.row, 4))) {
            this.updateStatus(false);
        }
        else {
            MsgBox.alert(this, "Bạn không có quyền được chỉnh sửa tài liệu này!");
        }
    }

    private void updateStatus (boolean insertable) {
        btnInsert.setEnabled(insertable);
        btnEdit.setEnabled(insertable);
        cboSubject.setEnabled(insertable);
        cboSubject.setEnabled(insertable);
        btnUpdate.setEnabled(!insertable);
        btnDelete.setEnabled(!insertable);
    }

    public boolean checkSameID (JTextField txt) {
        txt.setBackground(white);
        if (dao.selectById(Integer.parseInt(txt.getText())) == null) {
            return true;
        }
        else {
            txt.setBackground(getHSBColor(299, 21, 96));
            MsgBox.alert(this, txt.getName() + " đã bị tồn tại.");
            return false;
        }
    }
